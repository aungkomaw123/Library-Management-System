    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel D = (DefaultTableModel) jTable1.getModel();
        int selectIndex = jTable1.getSelectedRow();

        int bookID = Integer.parseInt(D.getValueAt(selectIndex, 0).toString());

        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            pst = con.prepareStatement("delete from book where bookID=?");
            pst.setInt(1, bookID);

            int k = pst.executeUpdate();

            if (k == 1) {
                JOptionPane.showMessageDialog(this, "Your Selected Book is Successfully Deleted");
                id.setText("");
                b_name.setText("");
                pub.setText("");
                b_price.setText("");
                b_publish_year.setText("");
                b_edition.setText("");
                load_Book();
            } else {
                JOptionPane.showMessageDialog(this, "Error");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_jButton3ActionPerformed

